public class W02_02_SumNumber {
public static void main(String[] args) {
    int x = 2; //ตัวเลย1
    int y = 998; //ตัวเลย2
    //ตัวแปรค่า
    int sum = x + y;
    //แสดงผลลัพธ์
    System.out.println ("ผลรวมของตัวเลข =" + sum) ;
    System.out.println ( "ผลรวมของ " + x + " + " + y + " = " + sum );
    
}


}
